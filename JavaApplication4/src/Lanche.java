public class Lanche {
    private String name;
    private int cod;
    private double price;

    public Lanche(String name, int cod, double price) {
        this.name = name;
        this.cod = cod;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getCod() {
        return cod;
    }

    public double getPrice() {
        return price;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    
    
    
    
    
}
